    # ==========================================
    # 1. U-Type & I-Type (Immediate) Test
    # ==========================================
    # LUI, AUIPC, ADDI のテスト
    lui   x1, 0x12345      # x1 = 0x12345000
    addi  x1, x1, 0x678    # x1 = 0x12345678
    auipc x2, 0            # x2 = PC (この時点でのPCアドレス)

    # ==========================================
    # 2. I-Type (ALU) Test
    # ==========================================
    # SLTI, SLTIU, XORI, ORI, ANDI, SLLI, SRLI, SRAI のテスト
    addi  x3, x0, -10      # x3 = -10 (0xFFFFFFF6)
    slti  x4, x3, -1       # x4 = 1 (-10 < -1)
    sltiu x5, x3, 1        # x5 = 0 (符号なしなら -10 は巨大な数)
    xori  x6, x4, 1        # x6 = 0 (1 ^ 1)
    ori   x7, x6, 0x0F     # x7 = 0x0000000F
    andi  x8, x7, 0x05     # x8 = 0x00000005
    slli  x9, x8, 4        # x9 = 0x00000050 (5 << 4)
    srli  x10, x9, 2       # x10 = 0x00000014 (80 >> 2)
    srai  x11, x3, 1       # x11 = 0xFFFFFFFB (-10 >>> 1 = -5)

    # ==========================================
    # 3. R-Type Test
    # ==========================================
    # ADD, SUB, SLL, SLT, SLTU, XOR, SRL, SRA, OR, AND のテスト
    add   x12, x9, x10     # x12 = 0x50 + 0x14 = 0x00000064 (100)
    sub   x13, x9, x10     # x13 = 0x50 - 0x14 = 0x0000003C (60)
    sll   x14, x10, x4     # x14 = 0x14 << 1   = 0x00000028 (40)
    slt   x15, x3, x12     # x15 = 1 (-10 < 100)
    sltu  x16, x3, x12     # x16 = 0 (符号なし比較)
    xor   x17, x12, x13    # x17 = 0x64 ^ 0x3C = 0x00000058
    srl   x18, x14, x4     # x18 = 0x28 >> 1   = 0x00000014
    sra   x19, x3, x4      # x19 = -10 >>> 1   = 0xFFFFFFFB (-5)
    or    x20, x12, x13    # x20 = 0x64 | 0x3C = 0x0000007C
    and   x21, x12, x13    # x21 = 0x64 & 0x3C = 0x00000024

    # ==========================================
    # 4. Load / Store Test
    # ==========================================
    # SW, LW, SH, LH, LHU, SB, LB, LBU のテスト
    addi  x22, x0, 64      # メモリアドレス 64番地を使用
    sw    x1, 0(x22)       # 64番地に 0x12345678 をストア
    lw    x23, 0(x22)      # x23 = 0x12345678 (正常なら x1 と一致)
    lh    x24, 0(x22)      # x24 = 0x00005678 (下位16bitロード、符号拡張)
    lhu   x25, 2(x22)      # x25 = 0x00001234 (上位16bitを符号なしロード)
    lb    x26, 0(x22)      # x26 = 0x00000078 (下位8bitロード)
    lbu   x27, 3(x22)      # x27 = 0x00000012 (最上位8bitを符号なしロード)

    # ==========================================
    # 5. Branch Test
    # ==========================================
    # BEQ, BNE, BLT, BGE, BLTU, BGEU のテスト
    # ※ もし分岐が失敗すると、直後の 0xBAD が x28 に書き込まれます
    addi  x28, x0, 0       # エラーフラグ初期化 (正常なら 0 のまま)

    beq   x23, x1, b_pass1 # 0x12345678 == 0x12345678 (Take)
    addi  x28, x0, 0xBAD   
    b_pass1:

    bne   x23, x0, b_pass2 # 0x12345678 != 0 (Take)
    addi  x28, x0, 0xBAD
    b_pass2:

    blt   x3, x0, b_pass3  # -10 < 0 (Take)
    addi  x28, x0, 0xBAD
    b_pass3:

    bge   x0, x3, b_pass4  # 0 >= -10 (Take)
    addi  x28, x0, 0xBAD
    b_pass4:

    # ==========================================
    # 6. Jump Test (JAL & JALR)
    # ==========================================
    jal   x29, j_target    # JALテスト: x29 に戻りアドレスを入れてジャンプ
    addi  x28, x0, 0xBAD   # ここはスキップされるはず
    j_target:

    jal   x30, jalr_setup  # JALRの準備用
    jalr_setup:
    addi  x30, x30, 12     # x30 のアドレスに +12 を加算
    jalr  x0, x30, 0       # JALRテスト: x30 のアドレスへジャンプ
    addi  x28, x0, 0xBAD   # ここはスキップされるはず
    jalr_target:           # JALRの着地地点

    # ==========================================
    # 7. End of Test
    # ==========================================
    addi  x31, x0, 0x777   # 全テスト完走フラグ (0x00000777)
    end:
    beq   x0, x0, end      # 無限ループでシミュレーション停止